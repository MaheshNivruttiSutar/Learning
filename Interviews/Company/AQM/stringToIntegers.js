//In JavaScript, you can convert a string to an integer using built-in functions. Here are some common methods:
// 1. Using parseInt()
let num = parseInt("12345");
console.log(num); // Output: 12345






// 2. Using Number()
let num1 = Number("12345");
console.log(num1); // Output: 12345


// 3. Using Unary + Operator
let num2 = +"12345";
console.log(num2); // Output: 12345


// 4. Using Math.floor() or Math.ceil()
let num5 = Math.floor("12345");
console.log(num5); // Output: 12345


// 5. Using ~~ (Bitwise NOT Operator)
let num6 = ~~"12345";
console.log(num6); // Output: 12345


console.log(parseInt("123"));      // Output: 123
console.log(Number("123"));       // Output: 123
console.log(+"123");              // Output: 123
console.log(Math.floor("123.45")) // Output: 123
console.log(~~"123");             // Output: 123

