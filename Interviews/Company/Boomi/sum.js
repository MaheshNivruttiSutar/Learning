/*
Given an Array - Sum Using Enhanced For Loop
This is a simple exercise to sum the elements of an array using the enhanced for loop.
Here's how you would do this in JavaScript and Java:
*/


const array = [1, 2, 3, 4, 5];
let sum = 0;

// Using enhanced for loop
for (let num of array) {
    sum += num;
}

console.log('Sum of array:', sum);  // Output: 15
